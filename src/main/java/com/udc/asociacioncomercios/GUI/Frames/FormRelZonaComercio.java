     private boolean validarCampos(JTextField campoNombre,JTextField campoCodigo){
            // Validar que los campos no estén vacíos
    if (campoNombre.getText().isEmpty() || campoCodigo.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    
    return true;
    } 
    
    public void actualizarTabla() {

        // Limpiar la tabla antes de agregar nuevos datos
        d.getModAsComercio().setRowCount(0);

        // Agregar los nuevos datos
        for (Comercio c: d.getListaZonas().get(posicion).getComerciosAsociados()) {
            
            d.agregarFilaAsComercio(c.getNombre(), c.getTipo(), c.getTelefono(), c.getEmail(), "Eliminar");
  
        }

        // Notificar a la tabla que los datos han cambiado
        d.getModAsComercio().fireTableDataChanged();
    }
    


