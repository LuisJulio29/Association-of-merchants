    private void addComercioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addComercioActionPerformed
        String nuevoComercio = (String) comercio.getSelectedItem();
        Comercio nc = d.buscarComercioPorNombre(nuevoComercio);
        d.getListaZonas().get(posicion).asignarComercio(nc);
        actualizarTabla();
    }//GEN-LAST:event_addComercioActionPerformed

    private void tAsociacionComercioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tAsociacionComercioMouseClicked
        int f = tAsociacionComercio.rowAtPoint(evt.getPoint());
        int c = tAsociacionComercio.columnAtPoint(evt.getPoint());
        
        if(c == 4){
            int posicionEncontrada = -1;
            String n = tAsociacionComercio.getValueAt(f, 0).toString();
            // Bucle foreach para buscar el comercio por nombre
            for (int i = 0; i < d.getListaZonas().get(posicion).getComerciosAsociados().size(); i++) {
                Comercio comercio = d.getListaZonas().get(posicion).getComerciosAsociados().get(i);
                if (comercio.getNombre().equalsIgnoreCase(n)) {
                    // El nombre del comercio coincide, guarda la posición y sal del bucle
                    posicionEncontrada = i;
                    break;
                }
            }
            if (posicionEncontrada != -1) {
                d.getListaZonas().get(posicion).getComerciosAsociados().remove(posicionEncontrada);
                actualizarTabla();
            }     
        }
    }//GEN-LAST:event_tAsociacionComercioMouseClicked

