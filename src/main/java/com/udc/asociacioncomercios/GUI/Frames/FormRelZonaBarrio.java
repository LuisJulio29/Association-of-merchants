    private void tAsociacionBarrioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tAsociacionBarrioMouseClicked
        int f = tAsociacionBarrio.rowAtPoint(evt.getPoint());
        int c = tAsociacionBarrio.columnAtPoint(evt.getPoint());
        
        if(c == 1){
            int posicionEncontrada = -1;
            String n = tAsociacionBarrio.getValueAt(f, 0).toString();

            for (int i = 0; i < d.getListaZonas().get(posicion).getBarrios().size(); i++) {
                String b = d.getListaZonas().get(posicion).getBarrios().get(i);
                if (b.equalsIgnoreCase(n)) {
                    posicionEncontrada = i;
                    break;
                }
            }
            if (posicionEncontrada != -1) {
                d.getListaZonas().get(posicion).getBarrios().remove(posicionEncontrada);
                actualizarTabla();
            }     
        }
    }//GEN-LAST:event_tAsociacionBarrioMouseClicked

