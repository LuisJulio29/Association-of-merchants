    public void actualizarTabla() {

        // Limpiar la tabla antes de agregar nuevos datos
        d.getModAsBarrio().setRowCount(0);

        // Agregar los nuevos datos
        for (String b: d.getListaZonas().get(posicion).getBarrios()) {
            
            d.agregarFilaAsBarrio(b, "Eliminar");
     
  
        }

        // Notificar a la tabla que los datos han cambiado
        d.getModAsBarrio().fireTableDataChanged();
    }
    


