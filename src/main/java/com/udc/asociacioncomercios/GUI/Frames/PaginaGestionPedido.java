    private void tablaPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPedidosMouseClicked
        int f = tablaPedidos.rowAtPoint(evt.getPoint());
        int c = tablaPedidos.columnAtPoint(evt.getPoint());
        
        if(c == 4){
            int posicionEncontrada = -1;
            String n = tablaPedidos.getValueAt(f, 0).toString();
            
            for (int i = 0; i < d.getListaProductos().size(); i++) {
                Pedido pe = d.getListaPedidos().get(i);
                if (pe.getIdPedido().equalsIgnoreCase(n)) {
                    posicionEncontrada = i;
                    break;
                }
            }
       
            if (posicionEncontrada != -1) {
                FormVerPedido ep = new FormVerPedido(posicionEncontrada);
                ep.actualizarTabla();
                ep.setVisible(true); 
            }     
        }
    }//GEN-LAST:event_tablaPedidosMouseClicked

