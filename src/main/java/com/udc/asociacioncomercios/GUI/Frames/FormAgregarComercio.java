    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 if (validarCampos(nombreComercio,telefonoComercio,emailComercio,barrioComercio,direccionAComercio)) {
                // Obtener valores de los campos
                String nuevoNombre = nombreComercio.getText();
                String nuevoTelefono = telefonoComercio.getText();
                String nuevoTipo = (String) tipoComercio.getSelectedItem();
                String nuevoEmail = emailComercio.getText();
                String nuevoBarrio = barrioComercio.getText();
                String nuevaDireccion = direccionAComercio.getText();
                
                if(nuevoTipo.equals("Supermercado")){
                    Supermercado ns = new Supermercado(nuevoNombre,nuevoTelefono,nuevoEmail,nuevoTipo,nuevoBarrio,nuevaDireccion);
                    d.agregarComercio(ns);
                }else if(nuevoTipo.equals("Pequeño comercio")){
                    PequeñoComercio np = new PequeñoComercio(nuevoNombre,nuevoTelefono,nuevoEmail,nuevoTipo,nuevoBarrio,nuevaDireccion);
                    d.agregarComercio(np);
                }
                d.agregarFilaComercio(nuevoNombre, nuevoTipo, nuevoTelefono, nuevoEmail, nuevoBarrio, "Editar");
                JOptionPane.showMessageDialog(null, "Comercio agregado exitosamente.", "COMERSCA", JOptionPane.DEFAULT_OPTION); 
                dispose();
    } 
    }//GEN-LAST:event_jButton1ActionPerformed

