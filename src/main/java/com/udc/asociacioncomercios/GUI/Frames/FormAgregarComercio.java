    private boolean validarCampos(JTextField campoNombre,JTextField campoTelefono,JTextField campoEmail,JTextField campoBarrio,JTextField campoDireccion){
            // Validar que los campos no estén vacíos
    if (campoNombre.getText().isEmpty() || campoTelefono.getText().isEmpty() || campoEmail.getText().isEmpty() || campoBarrio.getText().isEmpty() || campoDireccion.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    
    for(Comercio c: listaComercios){
        if(c.getNombre().equalsIgnoreCase(campoNombre.getText())){
            JOptionPane.showMessageDialog(null, "El Comercio ya se encuentra registrado.", "Error", JOptionPane.ERROR_MESSAGE);
            return false; 
        }
    }
    
    // Validar que el campo de correo electrónico contenga al menos un '@'
    if (!campoEmail.getText().contains("@")) {
        JOptionPane.showMessageDialog(null, "El campo de correo electrónico debe contener al menos un '@'.", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }
    
    return true;
    } 

