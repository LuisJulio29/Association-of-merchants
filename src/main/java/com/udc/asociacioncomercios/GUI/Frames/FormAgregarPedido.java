    private Comercio asignarComercio(Cliente c){
        d.ordenarZonas();
        
        if(!d.getListaZonas().isEmpty()){
            for(ZonaDeDistribucion z: d.getListaZonas()){
                for(Comercio com: z.getComerciosAsociados()){
                    if(com.getBarrio().equalsIgnoreCase(c.getBarrio())){
                        return com;
                    }
                }
            }
            return d.getListaZonas().get(0).getComerciosAsociados().get(0);
        }
        return null;
    }
    
