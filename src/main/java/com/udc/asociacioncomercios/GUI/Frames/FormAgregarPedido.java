    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            if(!idPedido.getText().isEmpty() || !fechaEnvio.getText().isEmpty()){
                String nuevoId = idPedido.getText();
                LocalDateTime nuevaFechaEmision = LocalDateTime.parse(fechaEmision.getText(), formatoFecha);
                LocalDateTime nuevaFechaEnvio = LocalDateTime.parse(fechaEnvio.getText(), formatoFecha);
                String nuevoCliente = (String)clientePedido.getSelectedItem();
                
                String[] datos = nuevoCliente.split(" ");
                Cliente cl = d.buscarClientePorNombre(datos[0]);
                
                Comercio ncom = asignarComercio(cl);
               
                ped.setIdPedido(nuevoId);
                ped.setFechaPedido(nuevaFechaEmision);
                ped.setFechaEnvio(nuevaFechaEnvio);
                ped.setComercio(ncom);
                ped.setCliente(cl);
                d.agregarPedido(ped);
                d.agregarFilaPedido(nuevoId, fechaEnvio.getText(), nuevoCliente, ncom.getNombre(), "Ver");
                JOptionPane.showMessageDialog(null, "Pedido agregado exitosamente.", "COMERSCA", JOptionPane.DEFAULT_OPTION); 
                ped = new Pedido();
                dispose();
            }
     
    }//GEN-LAST:event_jButton1ActionPerformed

