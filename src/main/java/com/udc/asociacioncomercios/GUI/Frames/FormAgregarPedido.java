    private void addProductoPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProductoPedidoActionPerformed
        
        if(!cantidadPedido.getText().isEmpty()){
            String nuevoProducto = (String) productoPedido.getSelectedItem();
            int nuevaCantidad = Integer.parseInt(cantidadPedido.getText());
            
            Producto productoDeseado = d.buscarProductoPorNombre(nuevoProducto);

            if(nuevaCantidad >= 0 && nuevaCantidad <= productoDeseado.getCantidadDisponible()){
                System.out.println(nuevaCantidad);
                ped.agregarProducto(productoDeseado);
                ped.getProductos().get(ped.getProductos().indexOf(productoDeseado)).setCantidadDisponible(nuevaCantidad);
                int pos = d.getListaProductos().indexOf(productoDeseado);
                d.getListaProductos().get(pos).setCantidadDisponible(d.getListaProductos().get(pos).getCantidadDisponible() - nuevaCantidad);
                d.agregarFilaProductoPedido(nuevoProducto, String.valueOf(nuevaCantidad));
            }else{
                JOptionPane.showMessageDialog(null, "Stock insuficiente o inexistente.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
              JOptionPane.showMessageDialog(null, "Por favor asigne una cantidad.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_addProductoPedidoActionPerformed

